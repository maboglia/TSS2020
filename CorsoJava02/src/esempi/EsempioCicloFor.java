package esempi;

public class EsempioCicloFor {

	public static void ciclaFor() {
		/*le 3 strutture fondamentali:
		 * sequenza, selezione, iterazione
		 */

		//3) iterazione con ciclo for
		
		for(int i = 0; i < 10; i++) {
			System.out.println("ciao " + i);
		}		
	}
	public static void main(String[] args) {
		ciclaFor();
	}
}
